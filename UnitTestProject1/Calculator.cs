using System;

namespace UnitTestProject1
{
    internal class Calculator
    {
        public Calculator()
        {
        }

        public int Add(int firstTerm, int secondTerm)
        {
            return firstTerm + secondTerm;
        }

        public int Subtract(int firstTerm, int secondTerm)
        {
            return firstTerm - secondTerm;
        }
    }
}